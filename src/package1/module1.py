"""Module with functions."""

def add_two_numbers(a, b):
    """Add two numbers.

    Args:
        a (int): First number
        b (int): Second number

    Returns:
        int: Sum of two numbers
    """
    c = a + b
    return c 


def multiply_two_numbers(num1, num2):
    """Multiplies two numbers.

    Args:
        num1 (int): First number
        num2 (int): Second number

    Returns:
        int: Product of two numbers.
    """
    product = num1 * num2
    return product 